package bai3;

public class HocSinh {
    public int soBaoDanh;
    public String hoTen;
    public String diaChi;
    public int mucUuTien;

    public HocSinh(int soBaoDanh, String hoTen, String diaChi, int mucUuTien) {
        this.soBaoDanh = soBaoDanh;
        this.hoTen = hoTen;
        this.diaChi = diaChi;
        this.mucUuTien = mucUuTien;
    }
}
